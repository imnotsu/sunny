    private void buscarCampanhaPorId() {
        try {
            int id = Integer.parseInt(SearchCampanhaPorIdTextField.getText().trim());
            CampanhaDAO dao = new CampanhaDAO();

            try (java.sql.ResultSet rs = dao.buscarPorId(id)) {
                DefaultTableModel model = new DefaultTableModel(
                    new Object[]{"ID", "Titulo", "Descricao", "Mes Ref.", "Meta Dinheiro", "Meta Alimentos", "Meta Roupas", "Meta Brinquedos", "Meta Livros", "Meta Calcados", "Meta Eletrodomesticos", "Meta Saude", "Ativa", "Id Admin", "Criado em"},
                    0
                ) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                };

                if (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getInt("id_campanha"),
                        rs.getString("titulo"),
                        rs.getString("descricao"),
                        rs.getDate("mes_referencia"),
                        rs.getBigDecimal("meta_dinheiro"),
                        rs.getInt("meta_alimentos"),
                        rs.getInt("meta_roupas"),
                        rs.getInt("meta_brinquedos"),
                        rs.getInt("meta_livros"),
                        rs.getInt("meta_calcados"),
                        rs.getInt("meta_eletrodomesticos"),
                        rs.getInt("meta_saude"),
                        rs.getBoolean("ativa"),
                        rs.getInt("id_admin"),
                        rs.getTimestamp("criado_em")
                    });

                    jTable1.setModel(model);
                    javax.swing.JOptionPane.showMessageDialog(this, "Campanha encontrada.");
                } else {
                    jTable1.setModel(model);
                    javax.swing.JOptionPane.showMessageDialog(this, "Campanha não encontrada.");
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao buscar campanha.");
        }
    }
    
