    private void EditarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditarBtnMouseClicked
        try {
            int idCampanha = Integer.parseInt(IdCampanhaTextField.getText().trim());
            String titulo = TituloTextField.getText().trim();
            String descricao = DescricaoTextField.getText().trim();
            java.sql.Date mesReferencia = java.sql.Date.valueOf("2026-06-01"); // ajuste depois se quiser pegar de um campo
            BigDecimal metaDinheiro = new BigDecimal(ValorMetaTextField.getText().trim().replace(",", "."));
            boolean ativa = AtivaCheckBox.isSelected();

            int metaAlimentos = 0;
            int metaRoupas = 0;
            int metaBrinquedos = 0;
            int metaLivros = 0;
            int metaCalcados = 0;
            int metaEletrodomesticos = 0;
            int metaSaude = 0;

            Object item = TipoDeMetaComboBox.getSelectedItem();
            String tipo = item != null ? item.toString() : "";

            if ("DINHEIRO".equalsIgnoreCase(tipo)) {
                metaDinheiro = new BigDecimal(ValorMetaTextField.getText().trim().replace(",", "."));
            } else if ("ALIMENTO".equalsIgnoreCase(tipo)) {
                metaAlimentos = Integer.parseInt(ValorMetaTextField.getText().trim());
            } else if ("ROUPA".equalsIgnoreCase(tipo)) {
                metaRoupas = Integer.parseInt(ValorMetaTextField.getText().trim());
            } else if ("BRINQUEDO".equalsIgnoreCase(tipo)) {
                metaBrinquedos = Integer.parseInt(ValorMetaTextField.getText().trim());
            } else if ("LIVRO".equalsIgnoreCase(tipo)) {
                metaLivros = Integer.parseInt(ValorMetaTextField.getText().trim());
            } else if ("CALCADO".equalsIgnoreCase(tipo)) {
                metaCalcados = Integer.parseInt(ValorMetaTextField.getText().trim());
            } else if ("ELETRODOMESTICO".equalsIgnoreCase(tipo)) {
                metaEletrodomesticos = Integer.parseInt(ValorMetaTextField.getText().trim());
            } else if ("SAUDE".equalsIgnoreCase(tipo)) {
                metaSaude = Integer.parseInt(ValorMetaTextField.getText().trim());
            }

            CampanhaDAO dao = new CampanhaDAO();
            boolean ok = dao.atualizar(idCampanha, titulo, descricao, mesReferencia, metaDinheiro,
                    metaAlimentos, metaRoupas, metaBrinquedos, metaLivros, metaCalcados,
                    metaEletrodomesticos, metaSaude, ativa);

            if (ok) {
                javax.swing.JOptionPane.showMessageDialog(this, "Campanha atualizada com sucesso!");
                carregarTabela();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Erro ao atualizar campanha.");
            }

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao editar campanha.");
        }
    }//GEN-LAST:event_EditarBtnMouseClicked

    private void ExcluirBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ExcluirBtnMouseClicked
        try {
            int idCampanha = Integer.parseInt(IdCampanhaTextField.getText().trim());

            int confirm = javax.swing.JOptionPane.showConfirmDialog(
                    this,
                    "Deseja excluir esta campanha?",
                    "Confirmar exclusão",
                    javax.swing.JOptionPane.YES_NO_OPTION
            );

            if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                CampanhaDAO dao = new CampanhaDAO();
                boolean ok = dao.excluir(idCampanha);

                if (ok) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Campanha excluída com sucesso.");
                    carregarTabela();
                    limparCampos();
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Erro ao excluir campanha.");
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao excluir.");
        }
    }//GEN-LAST:event_ExcluirBtnMouseClicked

